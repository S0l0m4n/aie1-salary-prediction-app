def _card(label: str, value: str, css_class: str = "accent") -> str:
    return (
        f'<div class="kpi-card">'
        f'<div class="kpi-label">{label}</div>'
        f'<div class="kpi-value {css_class}">{value}</div>'
        f"</div>"
    )


