def style_fig(fig: go.Figure, height: int = 400) -> go.Figure:
    fig.update_layout(
        template="plotly_dark",
        height=height,
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        margin=dict(l=8, r=8, t=40, b=8),
        font_family="Inter, system-ui, sans-serif",
        legend=dict(
            orientation="h",
            yanchor="bottom",
            y=1.02,
            xanchor="right",
            x=1,
        ),
    )
    return fig


def grouped_bar(
    data: pd.DataFrame,
    group_col: str,
    title: str,
    order: list | None = None,
    horizontal: bool = False,
) -> go.Figure:
    """Mean Actual vs Predicted salary grouped by group_col."""
    grp = (
        data.groupby(group_col)[["salary_in_usd", "predicted_salary_usd"]]
        .mean()
        .rename(
            columns={"salary_in_usd": "Actual", "predicted_salary_usd": "Predicted"}
        )
        .reset_index()
    )
    if order:
        grp[group_col] = pd.Categorical(grp[group_col], categories=order, ordered=True)
        grp = grp.sort_values(group_col)

    melted = grp.melt(
        id_vars=group_col,
        value_vars=["Actual", "Predicted"],
        var_name="Type",
        value_name="Mean Salary",
    )

    if horizontal:
        fig = px.bar(
            melted,
            y=group_col,
            x="Mean Salary",
            color="Type",
            barmode="group",
            orientation="h",
            color_discrete_map=PALETTE,
            title=title,
            labels={group_col: "", "Mean Salary": "Mean Salary (USD)", "Type": ""},
        )
        fig.update_xaxes(tickprefix="$", tickformat=",")
    else:
        fig = px.bar(
            melted,
            x=group_col,
            y="Mean Salary",
            color="Type",
            barmode="group",
            color_discrete_map=PALETTE,
            title=title,
            labels={group_col: "", "Mean Salary": "Mean Salary (USD)", "Type": ""},
        )
        fig.update_yaxes(tickprefix="$", tickformat=",")

    return style_fig(fig)


